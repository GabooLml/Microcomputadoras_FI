		PROCESSOR 16F877
		INCLUDE <P16F877.INC>

		ORG 0
		GOTO INICIO

		ORG 5
INICIO:	CLRF PORTA
		BSF STATUS, RP0
		BCF STATUS, RP1
		MOVLW H'0'
		MOVWF TRISB
		CLRF PORTB
		MOVLW 06H
		MOVWF ADCON1
		MOVLW H'3F'
		MOVWF TRISA
		BCF STATUS, RP0

LOOP:	BTFSC PORTA, 0
		GOTO ON
		MOVLW H'00'
		MOVWF PORTB
		GOTO LOOP

ON:		MOVLW H'FF'
		MOVWF PORTB
		GOTO LOOP
		END